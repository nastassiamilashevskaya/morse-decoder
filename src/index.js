const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let map = new Map(Object.entries(MORSE_TABLE));
    let result = '', code = '';

    for (let i = 0; i < expr.length; i += 10){
        code = expr.slice(i, i + 10);
        if (code === '**********') {
            result += ' ';
        } else {
            code = code.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-');
            result += map.get(code);
        }
    }

    return result;
}

module.exports = {
    decode
}